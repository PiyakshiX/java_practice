public class switchcase {
    public static void main(String[] args) {
        String fruit = "banana";
        switch(fruit) {
            case "apple":
                System.out.println("This is an apple.");
                break;
            case "banana":
                System.out.println("This is a banana.");
                break;
            case "orange":
                System.out.println("This is an orange.");
                break;
            default:
                System.out.println("Unknown fruit.");
        }
    
    }
}
