public class relational_operators {
    public static void main(String[] args){
        int a = 12;
        int b = 15;

        System.out.println(a == b);
        System.out.println(a != b);
        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a >= 12);
        System.out.println(b <= 20);
    }
    
}
